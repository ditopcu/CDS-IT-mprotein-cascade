            # --- Inner Optuna L2 (positive subset of outer train) ---
            pos_in_outer_train = np.array([i for i in outer_tri if i in set(pos_idx)])
            pos_in_outer_test  = np.array([i for i in outer_tei if i in set(pos_idx)])

