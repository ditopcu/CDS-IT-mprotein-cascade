                for j, idx in enumerate(outer_tei):
                    if l1_pos[j] and idx in set(pos_idx):
                        oi = pos_idx_list.index(idx)
                        l2p = m_l2.predict(X_l2[oi:oi+1])[0]
                        l3p = m_l3.predict_proba(X_l2[oi:oi+1])[0, 1]
                        h = L2_CLASSES[l2p]
                        l = L3_CLASSES[int(l3p >= 0.5)]
