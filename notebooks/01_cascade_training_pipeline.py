    print('=== Nested CV — Optuna ===')
    print('  5 outer folds × inner Optuna per fold')
    N_INNER_TRIALS = 50  # fewer trials per inner loop (vs 100 in single-loop)

    nested_oof_pred = np.full(N, 'NEGATIVE', dtype='U20')
